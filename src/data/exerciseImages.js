export function getExerciseImage(name) {
  const path = MAP[name];
  if (!path) return null;
  return `${BASE}/${path}/0.jpg`;
}

export function getExerciseImages(name) {
  const path = MAP[name];
  if (!path) return [];
  return [
    `${BASE}/${path}/0.jpg`,
    `${BASE}/${path}/1.jpg`,
  ];
}

